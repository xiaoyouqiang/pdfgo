// ResolveGlyph resolves a glyph name to a Unicode code point.
// It first checks the explicit AGL mapping, then handles special patterns:
//   - "uniXXXX" where XXXX are 4 hex digits (e.g., "uni4E2D" → U+4E2D)
//   - "uXXXXX" or "uXXXXXX" where X are 4-6 hex digits (e.g., "u1F600" → U+1F600)
func ResolveGlyph(name string) rune {
	if r, ok := GlyphToRune[name]; ok {
		return r
	}
	// Handle "uni" prefix: uniXXXX (4 hex digits)
	if strings.HasPrefix(name, "uni") && len(name) == 7 {
		v, err := strconv.ParseUint(name[3:], 16, 32)
		if err == nil && v > 0 && v <= 0x10FFFF {
			return rune(v)
		}
	}
	// Handle "u" prefix: uXXXXX (4-6 hex digits)
	if strings.HasPrefix(name, "u") && len(name) >= 5 && len(name) <= 7 {
		v, err := strconv.ParseUint(name[1:], 16, 32)
		if err == nil && v > 0 && v <= 0x10FFFF {
			return rune(v)
		}
	}
	return 0
}
